fun main() {
    val a = 500
    val b = 1000

    val result: Boolean = a > b // > = Lebih besar
    println(result)

    val result1: Boolean = a < b // < = Lebih kecil
    println(result1)

    val result2: Boolean = a >= b // Lebih dari sama dengan
    println(result2)

    val result3: Boolean = a <= b // Kurang dari sama dengan
    println(result3)

    val result4: Boolean = a == b // Sama dengan
    println(result4)

    val result5: Boolean = a != b // Tidak sama dengan
    println(result5)

    // Kalian juga bisa membuat operasi dengan printer seperti:
    println('a' > 'b')
    println(100 > 50)
}

Kesimpulan:

//> = Lebih besar
// < = Lebih kecil
// >= = Lebih dari sama dengan
// <= = Kurang dari sama dengan
// == = Sama dengan
// != = Tidak sama dengan
